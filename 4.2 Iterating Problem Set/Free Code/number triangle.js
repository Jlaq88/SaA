// Number Triangle
// This program will create a triangle of digits using while loops.  You will need a loop within a loop to do the trick
// OUTPUT:
// 0123456789
// 123456789
// 23456789
// 3456789
// 456789
// 56789
// 6789
// 789
// 89
// 9
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
let counter = 11;

while (counter <= 11){

    if (counter>10){
        console.log("0123456789")
    }
    else if (counter>9){
        console.log("123456789")
    }
    else if (counter>8){
        console.log("23456789")
    }
    else if (counter>7){
        console.log("3456789")
    }
    else if (counter>6){
        console.log("456789")
    }
    else if (counter>5){
        console.log("56789")
    }
    else if (counter>4){
        console.log("6789")
    }
    else if (counter>3){
        console.log("789")
    }
    else if (counter>2){
        console.log("89")
    }
    else if (counter>1){
        console.log("9")
    }
    else if (counter>0){
        console.log("Goodbye")
    }
    counter = counter-1
}




